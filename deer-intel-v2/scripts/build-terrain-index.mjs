// Regenerates lib/generated/terrainSets.ts from every
// terrain_movement.<name>.json the pipeline drops into lib/generated/.
//
// This is the "auto-import" glue: the app never needs a hand edit to register a
// new property's terrain prediction — drop the JSON in and rebuild this index.
// Runs from: `npm run terrain:index`, `npm run build` (prebuild), and the
// pipeline's run.sh.

import { mkdirSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const genDir = join(here, "..", "lib", "generated");
mkdirSync(genDir, { recursive: true });

const files = readdirSync(genDir)
  .filter((name) => /^terrain_movement\..+\.json$/.test(name))
  .sort();

// Light validation so a malformed drop fails loudly here, not at render time.
const valid = [];
for (const file of files) {
  try {
    const data = JSON.parse(readFileSync(join(genDir, file), "utf8"));
    if (!Array.isArray(data.features) || !Array.isArray(data.center)) {
      console.warn(`[terrain:index] skipping ${file}: missing features/center`);
      continue;
    }
    valid.push(file);
  } catch (error) {
    console.warn(`[terrain:index] skipping ${file}: ${error.message}`);
  }
}

const lines = [
  "// AUTO-GENERATED by scripts/build-terrain-index.mjs — do not edit by hand.",
  "// Regenerate with `npm run terrain:index` (also runs on prebuild and from the",
  "// terrain pipeline's run.sh). Register a property's prediction by dropping a",
  "// terrain_movement.<name>.json file into lib/generated/ and rebuilding.",
  'import type { TerrainMovementSet } from "@/lib/terrainMovement";',
  "",
];

valid.forEach((file, index) => {
  lines.push(`import set${index} from "./${file}";`);
});
if (valid.length > 0) lines.push("");

lines.push("export const GENERATED_TERRAIN_SETS: TerrainMovementSet[] = [");
valid.forEach((_, index) => {
  lines.push(`  set${index} as unknown as TerrainMovementSet,`);
});
lines.push("];");
lines.push("");

writeFileSync(join(genDir, "terrainSets.ts"), lines.join("\n"));
console.log(
  `[terrain:index] registered ${valid.length} generated terrain set(s)`,
);
